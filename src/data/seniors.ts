// Helper function to search seniors by name, skills, focus areas
export function searchSeniors(query: string): SeniorProfile[] {
    const q = query.toLowerCase();
    return seniors.filter(senior =>
        senior.name.toLowerCase().includes(q) ||
        senior.role.toLowerCase().includes(q) ||
        senior.focus.some(f => f.toLowerCase().includes(q)) ||
        senior.skills.some(s => s.toLowerCase().includes(q)) ||
        senior.bio.toLowerCase().includes(q) ||
        senior.company?.toLowerCase().includes(q)
    );
}

// Get senior by exact or partial name match
export function getSeniorByName(name: string): SeniorProfile | null {
    const normalized = name.toLowerCase().trim();
    return seniors.find(s =>
        s.name.toLowerCase() === normalized ||
        s.name.toLowerCase().includes(normalized) ||
        normalized.includes(s.name.toLowerCase().split(' ')[0].toLowerCase())
    ) || null;
}
